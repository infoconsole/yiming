public class SeriesLining {
    private String seriesid;
    private String seriescode;
    private String seriesname;
    private String seriescontent;
    private String liningcode;
    private String liningname;
    private String liningcolor;
    private String liningcolorurl;

    public String getSeriesid() {
        return seriesid;
    }

    public void setSeriesid(String seriesid) {
        this.seriesid = seriesid;
    }

    public String getSeriescode() {
        return seriescode;
    }

    public void setSeriescode(String seriescode) {
        this.seriescode = seriescode;
    }

    public String getSeriesname() {
        return seriesname;
    }

    public void setSeriesname(String seriesname) {
        this.seriesname = seriesname;
    }

    public String getSeriescontent() {
        return seriescontent;
    }

    public void setSeriescontent(String seriescontent) {
        this.seriescontent = seriescontent;
    }

    public String getLiningcode() {
        return liningcode;
    }

    public void setLiningcode(String liningcode) {
        this.liningcode = liningcode;
    }

    public String getLiningname() {
        return liningname;
    }

    public void setLiningname(String liningname) {
        this.liningname = liningname;
    }

    public String getLiningcolor() {
        return liningcolor;
    }

    public void setLiningcolor(String liningcolor) {
        this.liningcolor = liningcolor;
    }

    public String getLiningcolorurl() {
        return liningcolorurl;
    }

    public void setLiningcolorurl(String liningcolorurl) {
        this.liningcolorurl = liningcolorurl;
    }
}
